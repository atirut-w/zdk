any CodeGen::visitPrimaryExpression(CParser::PrimaryExpressionContext *ctx)
{
    if (auto const_ctx = ctx->Constant())
    {
        return parse_constant(const_ctx->getText());
    }
    else
    {
        throw runtime_error("unsupported expression type");
    }

    return any();
}
