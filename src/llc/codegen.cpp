#include <codegen.hpp>

using namespace std;
using namespace llvm;

CodeGen::CodeGen(unique_ptr<Module> &module, ostream &output) : module(module), output(output)
{
    output << "\t; Generated by ZDK LLVM IR compiler\n\n";
}

void CodeGen::generate()
{
    for (auto &function : module->functions())
    {
        output << "\t.global " << function.getName().str() << "\n";
        output << "\t.type " << function.getName().str() << ", @function\n";
        output << function.getName().str() << ":\n";

        // TODO: Detect "frame-pointer" attribute
        output << "\tpush ix\n";
        output << "\tld ix, 0\n";
        output << "\tadd ix, sp\n";

        // TODO: Implement function body

        // TODO: "frame-pointer" attribute, again
        output << "\tld sp, ix\n";
        output << "\tpop ix\n";
        output << "\tret\n\n";
    }
}
