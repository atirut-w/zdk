using namespace std;
using namespace llvm;

Codegen::Codegen(ostream &os) : os(os) {
  os << "\t; Generated by ZDK IR compiler\n\n";
}

std::any Codegen::visit_module(Module &module) {
  this->module = &module;
  for (auto &func : module) {
    if (func.isDeclaration()) {
      continue;
    }
    visit_function(func);
  }
  return nullptr;
}

std::any Codegen::visit_function(Function &function) {
  os << "\t.global " << function.getName().str() << "\n";
  os << "\t.type " << function.getName().str() << ", @function\n";
  os << function.getName().str() << ":\n";

  generate_prologue(function);

  for (auto &block : function) {
    for (auto &inst : block) {
      visit_instruction(inst);
    }
  }

  return {};
}

std::any Codegen::visit_instruction(Instruction &inst) {
  comment_instruction(inst);

  switch (inst.getOpcode()) {
  case Instruction::Load:
    os << "\t;   (Aliased to offset " << ctx.offsets[&inst] << ")\n";
    break;
  case Instruction::Ret:
    visit_return(cast<ReturnInst>(&inst));
    break;
  }

  return {};
}

void Codegen::generate_prologue(Function &function) {
  // TODO: Collect arguments

  int local_offset = 0;
  for (auto &block : function) {
    for (auto &inst : block) {
      if (auto *alloca = dyn_cast<AllocaInst>(&inst)) {
        ctx.offsets[alloca] = local_offset;
        local_offset -= module->getDataLayout().getTypeAllocSize(alloca->getAllocatedType());
      } else if (auto *load = dyn_cast<LoadInst>(&inst)) {
        auto *ptr = load->getPointerOperand();
        ctx.offsets[&inst] = ctx.offsets[ptr];
      } else if (!inst.getType()->isVoidTy()) {
        ctx.offsets[&inst] = local_offset;
        local_offset -= module->getDataLayout().getTypeAllocSize(inst.getType());
      }
    }
  }

  if (local_offset) {
    os << "\tpush ix\n";
    os << " \tld ix, 0\n";
    os << " \tadd ix, sp\n";

    os << " \tld hl, " << local_offset << "\n";
    os << " \tadd hl, sp\n";
    os << " \tld sp, hl\n";
  }

  return;
}

void Codegen::comment_instruction(Instruction &inst) {
  string str;
  raw_string_ostream rso(str);
  inst.print(rso);
  os << "\t; " << str << "\n";
}

void Codegen::visit_return(ReturnInst *inst) {
  // TODO: Load return value
  if (!ctx.offsets.empty()) {
    os << "\tld sp, ix\n";
    os << "\tpop ix\n";
  }
  os << "\tret\n";
}
