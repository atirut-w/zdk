Codegen::Codegen(ostream &os, Module *module) : os(os), module(module) {
  os << "\t; Generated by ZDK IR compiler\n\n";
}

void Codegen::pregen_function(Function &func) {
  ctx = {};

  for (auto &block : func) {
    for (auto &inst : block) {
      if (auto *alloc = dyn_cast<AllocaInst>(&inst)) {
        ctx.locals[alloc] = ctx.stack_size;

        Type *type = alloc->getAllocatedType();
        ctx.stack_size += module->getDataLayout().getTypeAllocSize(type);
      } else if (!inst.getType()->isVoidTy()) {
        ctx.locals[&inst] = ctx.stack_size;
        ctx.load_stat[&inst] = 0;

        Type *type = inst.getType();
        ctx.stack_size += module->getDataLayout().getTypeAllocSize(type);
      }
    }
  }
}

void Codegen::write_instruction(Instruction &inst) {
  string str;
  raw_string_ostream rso(str);
  inst.print(rso);
  os << "\t; " << rso.str() << "\n";
}

void Codegen::load(Value *val) {
  if (ctx.locals.find(val) != ctx.locals.end()) {
    if (ctx.load_stat[val])
      return;

    uint64_t size = module->getDataLayout().getTypeAllocSize(val->getType());
    uint8_t reg;
    if (size == 1)
      reg = ctx.allocator.allocate_r8();
    else if (size == 2)
      reg = ctx.allocator.allocate_r16();
    else if (size == 4) {
      vacate(Allocator::R16_DE | Allocator::R16_HL);
      reg = ctx.allocator.allocate(Allocator::R16_DE | Allocator::R16_HL);
    }

    assert_regs(reg);
    for (int i = 0; i < size; i++) {
      os << "\tld " << Allocator::register_names[reg][size - i - 1] << ", (iy+"
         << ctx.locals[val] + i << ")\n";
    }

    ctx.load_stat[val] = reg;
  } else if (auto *constant = dyn_cast<ConstantInt>(val)) {
    uint64_t value = constant->getZExtValue();
    uint64_t size = module->getDataLayout().getTypeAllocSize(val->getType());
    uint8_t reg;

    switch (size) {
    default:
      throw runtime_error("unsupported constant size");
    case 1: {
      reg = ctx.allocator.allocate_r8();
      os << "\tld " << Allocator::register_names[reg] << ", " << value << "\n";
      break;
    }
    case 2: {
      reg = ctx.allocator.allocate_r16();
      os << "\tld " << Allocator::register_names[reg] << ", " << value << "\n";
      break;
    }
    case 4:
      reg = ctx.allocator.allocate(Allocator::R16_DE | Allocator::R16_HL);
      os << "\tld hl, " << (value & 0xffff) << "\n";
      os << "\tld de, " << (value >> 16) << "\n";
    }

    ctx.load_stat[val] = reg;
  }
}

void Codegen::vacate(uint8_t regs) {
  vector<Value *> occupants;
  for (auto &pair : ctx.load_stat) {
    if (pair.second & regs) {
      occupants.push_back(pair.first);
    }
  }

  ctx.allocator.state |= regs;
  for (auto *occupant : occupants) {
    Type *type = occupant->getType();
    uint64_t size = module->getDataLayout().getTypeAllocSize(type);
    int offset = ctx.locals[occupant];

    uint8_t new_loc;
    if (size == 1)
      new_loc = ctx.allocator.allocate_r8();
    else if (size == 2)
      new_loc = ctx.allocator.allocate_r16();
    else if (size == 4)
      new_loc = ctx.allocator.allocate(Allocator::R16_DE | Allocator::R16_HL);

    if (new_loc) {
      for (int i = 0; i < size; i++) {
        os << "\tld " << Allocator::register_names[new_loc][size - i - 1]
           << ", "
           << Allocator::register_names[ctx.load_stat[occupant]][size - i - 1]
           << "\n";
      }
    } else {
      // "He bought?" "He went all in." "Dump it."
      for (int i = 0; i < size; i++) {
        os << "\tld (iy+" << offset + i << "), "
           << Allocator::register_names[ctx.load_stat[occupant]][size - i - 1]
           << "\n";
      }
    }

    if (new_loc)
      ctx.load_stat[occupant] = new_loc;
    else
      ctx.load_stat[occupant] = 0;
  }

  ctx.allocator.free(regs);
}

void Codegen::assert_regs(uint8_t regs) {
  if (regs)
    return;
  throw runtime_error("register allocation failed");
}

void Codegen::generate_function(Function &func) {
  os << "\t.global " << func.getName().str() << "\n";
  os << "\t.type " << func.getName().str() << ", @function\n";
  os << func.getName().str() << ":\n";
  ctx = {};

  pregen_function(func);
  if (ctx.stack_size) {
    os << "\tpush iy\n";
    os << "\tpush ix\n";

    os << "\tld ix, 0\n";
    os << "\tadd ix, sp\n";
    os << "\tld iy, -" << ctx.stack_size << "\n";
    os << "\tadd iy, sp\n";
    os << "\tld sp, iy\n";
  }

  for (auto &block : func) {
    for (auto &inst : block) {
      generate_instruction(inst);
    }
  }

  os << "\n\n";
}

void Codegen::generate_instruction(Instruction &inst) {
  write_instruction(inst);
  if (auto *alloc = dyn_cast<AllocaInst>(&inst)) {
    os << "\t;   (offset " << ctx.locals[alloc] << ")\n";
  } else if (auto *ret = dyn_cast<ReturnInst>(&inst)) {
    generate_return(ret);
  } else if (auto *load = dyn_cast<LoadInst>(&inst)) {
    generate_load(load);
  } else if (auto *store = dyn_cast<StoreInst>(&inst)) {
    generate_store(store);
  }
}

void Codegen::generate_return(ReturnInst *ret) {
  if (auto *val = ret->getReturnValue()) {
    load(val);
    uint64_t size = module->getDataLayout().getTypeAllocSize(val->getType());

    switch (size) {
    default:
      throw runtime_error("unsupported return size");
    case 1:
      if (ctx.load_stat[val] != Allocator::R8_A) {
        vacate(Allocator::R8_A);
        os << "\tld a, " << Allocator::register_names[ctx.load_stat[val]]
           << "\n";
      }
      break;
    case 2:
      if (ctx.load_stat[val] != Allocator::R16_HL) {
        vacate(Allocator::R16_HL);
        os << "\tld push " << Allocator::register_names[ctx.load_stat[val]]
           << "\n";
        os << "\tpop hl\n";
      }
      break;
    case 4:
      break; // Should already be in DEHL
    }
  }
  generate_epilogue();
  os << "\tret\n";
}

void Codegen::generate_epilogue() {
  if (ctx.stack_size) {
    os << "\tld sp, ix\n";
    os << "\tpop ix\n";
    os << "\tpop iy\n";
  }
}

void Codegen::generate_load(LoadInst *load) {
  Value *ptr = load->getPointerOperand();
  Type *type = load->getType();
  uint64_t size = module->getDataLayout().getTypeAllocSize(type);
  int offset = ctx.locals[ptr];

  uint8_t regs;
  if (size == 1)
    regs = ctx.allocator.allocate_r8();
  else if (size == 2)
    regs = ctx.allocator.allocate_r16();
  else if (size == 4)
    regs = ctx.allocator.allocate(Allocator::R16_DE | Allocator::R16_HL);

  for (int i = 0; i < size; i++) {
    os << "\tld " << Allocator::register_names[regs][size - i - 1] << ", (iy+"
       << offset + i << ")\n";
  }
  ctx.load_stat[load] = regs;
}

void Codegen::generate_store(StoreInst *store) {
  Value *src = store->getValueOperand();
  uint64_t size = module->getDataLayout().getTypeAllocSize(src->getType());

  if (auto *constant = dyn_cast<ConstantInt>(src)) {
    int offset = ctx.locals[store->getPointerOperand()];
    uint64_t value = constant->getZExtValue();

    for (int i = 0; i < size; i++) {
      os << "\tld (iy+" << offset + i << "), " << ((value >> (i * 8)) & 0xff)
         << "\n";
    }
  } else {
    load(src);
    int offset = ctx.locals[store->getPointerOperand()];

    for (int i = 0; i < size; i++) {
      os << "\tld (iy+" << offset + i << "), "
         << Allocator::register_names[ctx.load_stat[src]][size - i - 1] << "\n";
    }

    ctx.allocator.free(ctx.load_stat[src]);
    ctx.load_stat[src] = 0;
  }
}

void Codegen::generate() {
  for (auto &func : module->functions()) {
    if (func.isDeclaration()) {
      continue;
    }
    generate_function(func);
  }
}
